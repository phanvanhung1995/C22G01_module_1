let thuTrongTuan = parseInt(prompt('nhập vào thứ trong tuần'));

if (thuTrongTuan==1) {
    document.write('hôm nay là chủ nhật');
} else if (thuTrongTuan==2) {
    document.write('hôm nay là thứ 2');
} else if (thuTrongTuan==3) {
    document.write('hôm nay là thứ 3');
} else if (thuTrongTuan==4) {
    document.write('hôm nay là thứ 4');
} else if (thuTrongTuan==5) {
    document.write('hôm nay là thứ 5');
} else if (thuTrongTuan==6) {
    document.write('hôm nay là thứ 6');
} else if (thuTrongTuan==7) {
    document.write('hôm nay là thứ 7');
} else {
    document.write('không phải ngày trong tuần')
}
